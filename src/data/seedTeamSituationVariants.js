const typeToSuffix = {
  SPEED: 'speed',
  STRUCTURE: 'structure',
  BALANCE: 'balance',
  ALIGN: 'align'
};

function buildChoiceText(profile, frame) {
  return `${profile.choiceLens}을 기준으로 ${frame}`;
}

function buildChoiceTextOverrides(roundId, profile) {
  const frames = roundChoiceFrames[roundId] || {};
  return Object.fromEntries(
    Object.entries(frames).map(([type, frame]) => [`${roundId}_${typeToSuffix[type]}`, buildChoiceText(profile, frame)])
  );
}

export const seedTeamSituationVariants = Object.fromEntries(
  Object.keys(roundChoiceFrames).map(roundId => [
    roundId,
    Object.fromEntries(
      Object.entries(teamProfiles).map(([teamId, profile]) => [
        teamId,
        {
          teamId,
          signalText: profile.signals[roundId],
          choiceTextOverrides: buildChoiceTextOverrides(roundId, profile)
        }
      ])
    )
  ])
);
